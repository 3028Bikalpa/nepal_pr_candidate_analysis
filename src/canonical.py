_PARTY_TAIL_RE = re.compile(r"\s+(आम जनता पाट|जनता समाजवाद पाट|जनादेश पाट नेपाल|नागरक उमुित पाट, नेपाल|नागरक सवचता पाट नेपाल|नेपाल संघीय समाजवाद पाट|रािय मुित पाट नेपाल|बहुजन एकता पाट नेपाल|नेपाल जनजागृत पाट)$")


def _norm_text(s: str) -> str:
    s = unicodedata.normalize("NFKC", str(s or "")).strip()
    s = re.sub(r"[\(\)\[\]\{\},।'\"\-]", " ", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


def _key(s: str) -> str:
    return re.sub(r"\s+", "", _norm_text(s))


def _best_match(raw: str, canonical: Iterable[str], cutoff: float) -> Optional[str]:
    canonical = list(canonical)
    if not raw:
        return None
    raw_key = _key(raw)
    if not raw_key:
        return None

    best_name = None
    best_score = 0.0
    for cand in canonical:
        score = difflib.SequenceMatcher(None, raw_key, _key(cand)).ratio()
        if score > best_score:
            best_name = cand
            best_score = score
    return best_name if best_score >= cutoff else None


def canonicalize_party(raw: str) -> Optional[str]:
    s = _norm_text(raw)
    if not s:
        return None
    if s in _PARTY_ALIAS:
        return _PARTY_ALIAS[s]
    return _best_match(s, CANONICAL_PARTIES, cutoff=0.56)


def _preprocess_district(raw: str) -> list[str]:
    s = _norm_text(raw)
    if not s:
        return []

    for prefix in _GROUP_PREFIXES:
        p = f"{prefix} "
        if s.startswith(p):
            s = s[len(p):].strip()
            break

    s = _PARTY_TAIL_RE.sub("", s).strip()

    # Known OCR patterns for East/West Nawalparasi and old Rukum splits in source text.
    s = s.replace("नवलपरासी (बदघाट सु ता पूव)", "नवलपुर")
    s = s.replace("नवलपरासी (बदघाट सुता पूव)", "नवलपुर")
    s = s.replace("नवलपरासी (बदघाट सु ता पिचम)", "नवलपरासी")
    s = s.replace("नवलपरासी (बदघाट सुता पिचम)", "नवलपरासी")
    s = s.replace("नवलपरासी पूव", "नवलपुर")
    s = s.replace("पिचम कुम", "रुकुम")
    s = s.replace("कुम (पूव भाग)", "रुकुम")
    s = s.replace("कुम (पिचम भाग)", "रुकुम")

    tokens = s.split()
    candidates = [s]
    for n in (1, 2, 3, 4):
        if len(tokens) >= n:
            candidates.append(" ".join(tokens[:n]))

    out = []
    for c in candidates:
        c = _DISTRICT_ALIAS.get(c, c)
        if c and c not in out:
            out.append(c)
    return out


def canonicalize_district(raw: str) -> Optional[str]:
    candidates = _preprocess_district(raw)
    if not candidates:
        return None

    for c in candidates:
        if c in CANONICAL_DISTRICTS:
            return c

    best_name = None
    best_score = 0.0
    for c in candidates:
        c_key = _key(c)
        if not c_key:
            continue
        for d in CANONICAL_DISTRICTS:
            score = difflib.SequenceMatcher(None, c_key, _key(d)).ratio()
            if score > best_score:
                best_name = d
                best_score = score

    return best_name if best_score >= 0.58 else None


def standardize_party_district(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()
    out["party_raw"] = out["party"].fillna("").astype(str).str.strip()
    out["district_raw"] = out["नागरिकता जारी जिल्ला"].fillna("").astype(str).str.strip()

    out["party"] = out["party_raw"].map(canonicalize_party)
    out["नागरिकता जारी जिल्ला"] = out["district_raw"].map(canonicalize_district)

    out["party"] = out["party"].fillna("UNKNOWN_PARTY")
    out["नागरिकता जारी जिल्ला"] = out["नागरिकता जारी जिल्ला"].fillna("UNKNOWN_DISTRICT")
    return out
